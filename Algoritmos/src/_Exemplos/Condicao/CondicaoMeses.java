package _Exemplos.Condicao;

public class CondicaoMeses {

	public static void main(String[] args) {
		
		int mes = 7;
				switch(mes){
				case 7: System.out.println("Julho");
						break;
				case 8: System.out.println("Agosto");
						break;
					default: System.out.println("Mês inexistente.");
						break;
				}

	}
}
